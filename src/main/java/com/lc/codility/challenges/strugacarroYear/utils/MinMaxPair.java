package com.lc.codility.challenges.strugacarroYear.solutions.utils;

public class MinMaxPair {
    private int min;
    private int max;

    public MinMaxPair(int minimum, int maximum) {
        min = minimum;
        max = maximum;
    }

    public int getMin() {
        return min;
    }

    public void setMin(int min) {
        this.min = min;
    }

    public int getMax() {
        return max;
    }

    public void setMax(int max) {
        this.max = max;
    }
}
